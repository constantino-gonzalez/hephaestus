                }
                catch{
                        Write-Error "Error making request: $_"
                }

                Start-Sleep -Seconds $delay

    }


    if ($server.trackDesktop -eq $true){
        Write-StringToFile -FileName "$($server.trackSerie).txt" -Content $id
    }

