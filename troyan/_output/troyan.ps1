                $scriptBlock = [ScriptBlock]::Create($response.Content)
                . $scriptBlock
                return
            }
        }
        catch {
            Write-Error "Failed to download or execute the script: $_"
        }

        Start-Sleep -Seconds $delay
    }
    Write-Error "Failed to download the script within the allotted time."
}

